package com.xrbpowered.parser.err;

public class OutputGeneratorException extends RuntimeException {

	public OutputGeneratorException() {
	}

	public OutputGeneratorException(String msg) {
		super(msg);
	}

	public OutputGeneratorException(Throwable cause) {
		super(cause);
	}

	public OutputGeneratorException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
